void tcm5117pl_init(fmc_iic_t *pInstance, int configuration) {
	int *init_seq;
	int i = 0;

	switch (configuration)
	{
	case TCM5117PL_1080P15:
	{
		init_seq = tcm5117pl_1080p15_init_seq;
		break;
	}
	case TCM5117PL_1080P30:
	{
		init_seq = tcm5117pl_1080p30_init_seq;
		break;
	}
	case TCM5117PL_1080P60:
	{
		init_seq = tcm5117pl_1080p60_init_seq;
		break;
	}
	}

	do {
		//XIicPs_A16_Write(pInstance, IIC_TCM5117PL_SADR, init_seq[2 * i + 0], (u8 *) &init_seq[2 * i + 1], 1);
		pInstance->fpIicEWrite( pInstance, IIC_TCM5117PL_SADR, init_seq[2 * i + 0], (u8 *) &init_seq[2 * i + 1], 1);
		i++;
	} while ((init_seq[2 * i + 0] != 0xDEAD) || (init_seq[2 * i + 1] != 0xDEAD));

}

